function renderLinkGroups() {
  const container = document.getElementById("link-groups");
  for (const group of LINK_GROUPS) {
    const section = document.createElement("div");
    section.className = "link-group";

    const heading = document.createElement("h3");
    heading.textContent = group.title;
    section.appendChild(heading);

    const list = document.createElement("ul");
    for (const item of group.items) {
      const li = document.createElement("li");
      const a = document.createElement("a");
      a.href = item.link;
      a.textContent = item.name;
      a.target = "_blank";
      a.rel = "noopener noreferrer";
      li.appendChild(a);
      list.appendChild(li);
    }
    section.appendChild(list);
    container.appendChild(section);
  }
}

function renderAlbums() {
  const container = document.getElementById("albums");
  const modal = document.getElementById("lyrics-modal");
  const modalTitle = document.getElementById("modal-title");
  const modalLyrics = document.getElementById("modal-lyrics");

  for (const song of SONG_LYRICS) {
    const figure = document.createElement("figure");
    figure.className = "album";

    const button = document.createElement("button");
    button.type = "button";
    button.className = "album-trigger";

    const img = document.createElement("img");
    img.src = song.image;
    img.alt = `Album cover for ${song.title}`;
    img.className = "album-image";
    button.appendChild(img);

    button.addEventListener("click", () => {
      modalTitle.textContent = song.title;
      modalLyrics.textContent = song.lyrics;
      modal.showModal();
    });

    const caption = document.createElement("figcaption");
    caption.className = "album-caption";
    caption.textContent = song.title;

    figure.appendChild(button);
    figure.appendChild(caption);
    container.appendChild(figure);
  }

  document.getElementById("modal-close").addEventListener("click", () => modal.close());
  modal.addEventListener("click", (event) => {
    if (event.target === modal) modal.close();
  });
}

function initScrollReveal() {
  const targets = document.querySelectorAll(".reveal");
  const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;

  if (reduceMotion || !("IntersectionObserver" in window)) {
    targets.forEach((el) => el.classList.add("in-view"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      for (const entry of entries) {
        if (entry.isIntersecting) {
          entry.target.classList.add("in-view");
          observer.unobserve(entry.target);
        }
      }
    },
    { threshold: 0.15 }
  );

  targets.forEach((el) => observer.observe(el));
}

